	P.INTERFACE_ID,
	R.PLUGIN_ID,
	R.RULE_ID,
	R.TAG$01,
	R.TAG$02,
	R.TAG$03,
	R.TAG$04,
	R.TAG$05,
	R.TAG$06,
	R.TAG$07,
	R.TAG$08,
	R.TAG$09,
	R.TAG$10,
	R.TAG$11,
	R.TAG$12,
	R.TAG$13,
	R.TAG$14,
	R.TAG$15,
	R.TAG$16
FROM
	VPI.EXTRACT_PLUGIN		P,
	VPI.EXTRACT_RULE		R
WHERE
	P.PLUGIN_ID		= R.PLUGIN_ID
;
